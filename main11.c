/*
Useful syntax for using if and else
*/


#include  <stdio.h>


// This is an easy syntax for using if and else 
// condition ? if true: if false
int main()
{
    int number1=10;
    int number2=20;

    number2>number1 ? printf("number2 is greater than number1") : printf("Number1 is greater than number1");
}